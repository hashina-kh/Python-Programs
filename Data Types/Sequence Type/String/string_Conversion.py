#String Conversion Function in Python
mystring = "indroduction to PYTHON"
print("Our String is:", mystring)

print("------------String Conversion Function in Python -----------")
print("Using upper() method out string is:",mystring.upper())
print("Using lower() method out string is:",mystring.lower())
print("Using swapcase() method out string is:",mystring.swapcase())
print("Using title() method out string is:",mystring.title())
print("Using capitalize() method out string is:",mystring.capitalize())
