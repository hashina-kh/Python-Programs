x = "Python"
print("--------------String Comparison Function in Python ------------")
print("Our String is:",x)

print("Our isupper() method :", x.isupper())
print("Our islower() method :", x.islower())
print("Our isalpha() method :", x.isalpha())
print("Our isalnum() method :", x.isalnum())
print("Our isnumeric() method :", x.isnumeric())


