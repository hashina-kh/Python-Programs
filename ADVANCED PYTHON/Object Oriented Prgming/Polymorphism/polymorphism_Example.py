class India:
    def Capital(self):
        print("New Delhi is the Capital of India")
    def Language(self):
        print("Hindhi is the Widly used Language in India")

class USA:
    def Capital(self):
        print("Washington is the Capital of USA")
    def Language(self):
        print("English is the Widly used Language in USA")

obj = India()
obj.Capital()
obj.Language()

print()
obj2 = USA()
obj2.Capital()
obj2.Language()

